#include<stdio.h>
int main()
{
    char c[][5]={{'*','*','*','*','*'},
                 {'*',' ',' ',' ','*'},
                 {'*',' ',' ',' ','*'}, 
                 {'*',' ',' ',' ','*'},
                 {'*','*','*','*','*'}};
    int i,j;
    for(i=0;i<5;i++)//每一行
      {
           for(j=0;j<5;j++)//每一列
               printf("%c  ",c[i][j]);
           printf("\n");
      }

    return 0;  
}
